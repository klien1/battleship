export default word => {
  const specialChar = {
    "\\": "\\",
    "+": "+",
    "*": "*",
    "?": "?",
    "[": "[",
    "^": "^",
    "]": "]",
    $: "$",
    "(": "(",
    ")": ")",
    "{": "{",
    "}": "}",
    "=": "=",
    "!": "!",
    "<": "<",
    ">": ">",
    "|": "|",
    ":": ":",
    "-": "-",
    ".": ".",
    "/": "/",
    ";": ";",
    ",": ",",
    '"': '"',
    "'": "'",
    "@": "@",
    "#": "#",
    "%": "%",
    "&": "&",
    "`": "`",
    "~": "~"
  };

  return specialChar.hasOwnProperty(word[word.length - 1]);
};
